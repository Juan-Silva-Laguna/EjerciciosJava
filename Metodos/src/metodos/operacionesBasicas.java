
package metodos;

public class operacionesBasicas {
    static int suma(int val1, int val2){
        return (val1+val2);
    }
    static int divi(int val1, int val2){
        return (val1/val2);
    }
    static int resta(int val1, int val2){
        return (val1-val2);
    }
    static int multi(int val1, int val2){
        return (val1*val2);
    }
}
