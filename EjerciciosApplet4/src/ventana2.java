    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String mar = marca.getText();
        String mod = modelo.getText();
        String mem = memoria.getText();
        String pre = precio.getText();
        
        int N = tot.length;
        tot = Arrays.copyOf(tot, N + 1);
        tot[N] = Integer.parseInt(pre);
        
        datos += (cuenta+1)+"     "+mar+"    "+mod+"     "+mem+"     $"+pre+" \n";
        int cont = 0;
        for (int i = 0; i < tot.length; i++) {
            cont += tot[i];
        } 
        total.setText("$ "+cont);
        mostrar.setText(datos);
        
        marca.setText("");
        modelo.setText("");
        memoria.setText(""); 
        precio.setText(""); 
    }//GEN-LAST:event_jButton1ActionPerformed

